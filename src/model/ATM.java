package model;

import java.math.BigDecimal;

public class ATM {
    private int id;
    private BigDecimal saldo;

    public ATM(int id, BigDecimal saldo) {
        this.id = id;
        this.saldo = saldo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public BigDecimal getSaldo() {
        return saldo;
    }

    public void setSaldo(BigDecimal saldo) {
        this.saldo = saldo;
    }
}
